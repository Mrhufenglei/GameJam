package com.mop.game.client.buildtool.util;

public class MetaInfo {

	private String filename;
	private String SheetName;
	private int sheetIdx;
	private String id;
	private String columnIdx;
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getSheetName() {
		return SheetName;
	}
	public void setSheetName(String sheetName) {
		SheetName = sheetName;
	}
	public int getSheetIdx() {
		return sheetIdx;
	}
	public void setSheetIdx(int sheetIdx) {
		this.sheetIdx = sheetIdx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getColumnIdx() {
		return columnIdx;
	}
	public void setColumnIdx(String columnIdx) {
		this.columnIdx = columnIdx;
	}
	
	
	
	
}
